class Solution:
    # T(M*N)
    # S(M*N)
    def findLength(self, nums1: List[int], nums2: List[int]) -> int:
        if not nums1 or not nums2:
            return 0
        M, N = len(nums1), len(nums2)
        dp = [[0 for _ in range(N+1)] for _ in range(M+1)]
        for i in range(M-1, -1, -1):
            for j in range(N-1, -1, -1):
                if nums1[i] == nums2[j]:
                    dp[i][j] = dp[i+1][j+1] + 1
        return max([max(dp[i]) for i in range(M)])

    #@lru_cache()
    def is_subarray(self, subarray:List[int], array:List[int]) -> bool:
        self.cache = {}
        st = tuple(subarray)
        if st in self.cache:
            return self.cache[st]
        if len(subarray) > len(array):
            self.cache[st] = False
            return False
        for i in range(len(array)-len(subarray)+1):
            if array[i:i+len(subarray)] == subarray:
                self.cache[st] = True
                return True
        self.cache[st] = False
        return False

    # TLE. slow
    def findLength3(self, nums1: List[int], nums2: List[int]) -> int:
        if not nums1 or not nums2:
            return 0
        subarrays = [[], ]
        cur_max = 0
        for i in range(len(nums1)):
            subarrays2 = []
            for sa in subarrays:
                new_sa = sa
                new_sa.append(nums1[i])
                if self.is_subarray(new_sa, nums2):
                    subarrays2.append(new_sa)
                    cur_max = max(cur_max, len(new_sa))
            if nums1[i] in nums2 and nums1[i] not in subarrays2:
                subarrays2.append([nums1[i]])
                cur_max = max(cur_max, 1)
            subarrays = subarrays2
        return cur_max

    # Record sub lens -- Time Limit Exceed
    def findLength1(self, nums1: List[int], nums2: List[int]) -> int:
        if not nums1 or not nums2:
            return 0
        dp = [0 for _ in range(len(nums1))]
        sublens = [[] for _ in range(len(dp))]
        if nums1[0] in nums2:
            dp[0] = 1
            sublens[0].append(1)
        s2 = '_'.join([str(i) for i in  nums2])
        for i in range(1, len(dp)):
            for l in sublens[i-1]:
                sub = '_'.join([str(i) for i in nums1[i-l:i+1]])
                if sub in s2:
                    sublens[i].append(l+1)
            if nums1[i] in nums2:
                sublens[i].append(1)
            dp[i] = max(sublens[i]) if sublens[i] else 0
        return max(dp)

    # Record sub strs -- Momory Limit Exceed
    def findLength2(self, nums1: List[int], nums2: List[int]) -> int:
        dp = [0 for _ in range(len(nums1))]
        sub_arrays = [[] for _ in range(len(dp))]
        for i in range(len(dp)):
            if i == 0:
                if nums1[0] in nums2:
                    dp[0] = 1
                    if nums1[0] not in sub_arrays[0]:
                        sub_arrays[0].append([nums1[0]])
            else:
                cur_max = dp[i-1]
                if nums1[i] in nums2 and nums1[i] not in sub_arrays:
                    sub_arrays[i].append([nums1[i]])
                    cur_max = max(cur_max, 1)
                for ss in sub_arrays[i-1]:
                    new_array = ss + [nums1[i]]
                    if self.is_subarray(new_array, nums2):
                        sub_arrays[i].append(new_array)
                        cur_max = max(cur_max, len(new_array))
                dp[i] = cur_max
        return max(dp) if dp else 0


